# 4. Основна логіка: генеруємо файл для кожного дня і для кожного місяця
# —————————————————————————————————————————————————————————————————————————————

def write_xml(tree: ET.Element, path: str):
    """
    Серіалізує ElementTree у файл з відступами (pretty‐print).
    """
    ET.indent(tree, space="  ")
    xml_str = ET.tostring(tree, encoding="utf-8", xml_declaration=True)
    with open(path, "wb") as f:
        f.write(xml_str)


def generate_all(year: int):
    # 4.1. Збираємо дані по місяцях
    # Створюємо структуру { (r, m) : { "YYYY-MM-DD": [свята...] } }
    month_map = {}
    for date_str, celebrations in CELEBRATIONS.items():
        dt = datetime.datetime.fromisoformat(date_str).date()
        if dt.year != year:
            continue
        key = (dt.year, dt.month)
        month_map.setdefault(key, {})[date_str] = celebrations

    # 4.2. Генеруємо day/*.xml
    start = datetime.date(year, 1, 1)
    end = datetime.date(year, 12, 31)
    delta = (end - start).days + 1

    for d_offset in range(delta):
        current = start + datetime.timedelta(days=d_offset)

        # 1) Залишаємо дефіси для елементу <DateISO>
        iso_for_xml = current.isoformat()  # "2025-05-27"
        celebrations = CELEBRATIONS.get(iso_for_xml, [])
        tree = make_day_xml(current, celebrations)

        # 2) Для імені файлу форматуємо з підкресленнями
        filename_date = current.strftime("%Y_%m_%d")  # "2025_05_27"
        filename = f"{filename_date}.xml"
        fullpath = os.path.join(OUTPUT_DAY_DIR, filename)

        write_xml(tree, fullpath)
        print(f"Created day XML: {filename}")


    # 4.3. Генеруємо month/*.xml
    for (yy, mm), month_days in month_map.items():
        tree = make_month_xml(yy, mm, month_days)
        month_str = f"{yy}_{mm:02d}"
        filename = f"{month_str}.xml"
        fullpath = os.path.join(OUTPUT_MONTH_DIR, filename)
        write_xml(tree, fullpath)
        print(f"Created month XML: {filename}")


