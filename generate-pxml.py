def make_day_xml(date_obj: datetime.date, celebrations: list) -> ET.Element:
    """
    Створює кореневий елемент LHData з одним CalendarDay для given date.
    celebrations = список словників із ключами title, season, level, type.
    """
    # Створюємо кореневий контейнер
    root = ET.Element("LHData")

    # CalendarDay
    cal = ET.SubElement(root, "CalendarDay")

    # DateISO, DateDay, DateMonth, DateYear, DayOfYear
    iso = date_obj.isoformat()
    ET.SubElement(cal, "DateISO").text = iso
    ET.SubElement(cal, "DateDay").text = str(date_obj.day)
    ET.SubElement(cal, "DateMonth").text = str(date_obj.month)
    ET.SubElement(cal, "DateYear").text = str(date_obj.year)
    ET.SubElement(cal, "DayOfYear").text = str(date_obj.timetuple().tm_yday)

    # DayOfWeek: українською
    WEEKDAYS_UA = ["понеділок", "вівторок", "середа", "четвер", "п’ятниця", "субота", "неділя"]
    weekday_ua = WEEKDAYS_UA[date_obj.weekday()]
    dow = ET.SubElement(cal, "DayOfWeek", Id=str(date_obj.weekday()))
    dow.text = weekday_ua

    # Якщо є святкові дані
    for idx, item in enumerate(celebrations or []):
        # для кожного свята у цьому дні створюємо <Celebration>
        c = ET.SubElement(cal, "Celebration")
        ET.SubElement(c, "Id").text = str(idx)
        # Назва (StringTitle)
        st = ET.SubElement(c, "StringTitle")
        span_bold = ET.SubElement(st, "span", {"class": "bold"})
        span_upper = ET.SubElement(span_bold, "span", {"class": "uppercase"})
        span_upper.text = item["title"]
        # Сезон (LiturgicalSeason)
        ET.SubElement(c, "LiturgicalSeason").text = item["season"]
        # Рівень (LiturgicalCelebrationLevel)
        ET.SubElement(c, "LiturgicalCelebrationLevel").text = str(item["level"])
        # Тип (LiturgicalCelebrationType)
        ET.SubElement(c, "LiturgicalCelebrationType").text = item["type"]

    return root


def make_month_xml(year: int, month: int, month_days: dict) -> ET.Element:
    """
    Створює місячний XML, що містить усі <CalendarDay> для даного року+місяця,
    в тому числі ті, які не мають свят (але без <Celebration>).
    month_days: словник { "YYYY-MM-DD": [ список свят ] } для цього місяця.
    """
    root = ET.Element("LHData")

    # Ітерація по днях у місяці
    first = datetime.date(year, month, 1)
    # визначаємо кількість днів у місяці (перехід на наступний місяць мінус 1 день)
    if month == 12:
        next_month = datetime.date(year + 1, 1, 1)
    else:
        next_month = datetime.date(year, month + 1, 1)
    delta = (next_month - first).days

    for day_offset in range(delta):
        current = first + datetime.timedelta(days=day_offset)
        iso = current.isoformat()

        # Створюємо <CalendarDay> всередині root
        cal = ET.SubElement(root, "CalendarDay")
        ET.SubElement(cal, "DateISO").text = iso
        ET.SubElement(cal, "DateDay").text = str(current.day)
        ET.SubElement(cal, "DateMonth").text = str(current.month)
        ET.SubElement(cal, "DateYear").text = str(current.year)
        ET.SubElement(cal, "DayOfYear").text = str(current.timetuple().tm_yday)

        WEEKDAYS_UA = ["понеділок", "вівторок", "середа", "четвер", "п’ятниця", "субота", "неділя"]
        weekday_ua = WEEKDAYS_UA[current.weekday()]
        dow = ET.SubElement(cal, "DayOfWeek", Id=str(current.weekday()))
        dow.text = weekday_ua

        # Додаємо свята (якщо є)
        for idx, item in enumerate(month_days.get(iso, [])):
            c = ET.SubElement(cal, "Celebration")
            ET.SubElement(c, "Id").text = str(idx)
            st = ET.SubElement(c, "StringTitle")
            span_bold = ET.SubElement(st, "span", {"class": "bold"})
            span_upper = ET.SubElement(span_bold, "span", {"class": "uppercase"})
            span_upper.text = item["title"]
            ET.SubElement(c, "LiturgicalSeason").text = item["season"]
            ET.SubElement(c, "LiturgicalCelebrationLevel").text = str(item["level"])
            ET.SubElement(c, "LiturgicalCelebrationType").text = item["type"]

    return root


